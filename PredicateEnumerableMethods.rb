метод обозначается вопросительным знаком (?) в конце имени метода и возвращает либо true, либо false

Метод #include? работает точно так, как вы думаете. Если мы хотим узнать, существует ли определённый элемент в массиве, мы можем использовать метод #include?. 
Этот метод вернёт true, если элемент, который вы передаёте в качестве аргумента методу #include?, существует в массиве или хэше; в противном случае он вернёт false.

Сначала давайте рассмотрим, как мы могли бы это сделать с помощью метода #each
	
